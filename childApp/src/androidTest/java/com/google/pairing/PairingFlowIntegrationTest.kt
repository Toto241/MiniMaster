@RunWith(AndroidJUnit4::class)
class PairingFlowIntegrationTest {

    @get:Rule
    val composeTestRule = createAndroidComposeRule<MainActivity>()

    private lateinit var context: Context
    private lateinit var firestore: FirebaseFirestore

    @Before
    fun setUp() {
        context = ApplicationProvider.getApplicationContext()
        FirebaseApp.initializeApp(context) // Ensure Firebase is initialized for tests
        firestore = Firebase.firestore // Use the real Firestore instance

        // Clear DataStore to ensure starting on PairingScreen
        runBlocking {
            context.dataStore.edit { preferences ->
                preferences.clear()
            }
        }
        // It's good practice to use a Firestore emulator for tests.
        // For this example, we'll assume it's configured or use the real dev instance carefully.
        // try { firestore.useEmulator("10.0.2.2", 8080) } catch (e: IllegalStateException) { /* Already configured */ }
    }

    @Test
    fun successfulPairingFlow_navigatesToLockScreen() {
        val pairingCode = "INTEGRATION${UUID.randomUUID().toString().take(6)}"
        val childIdToSave = "child-${UUID.randomUUID()}"

        // 1. Setup Firestore with a valid, non-expired pairing code
        runBlocking {
            val codeData = hashMapOf(
                "childId" to childIdToSave, // This would typically be generated by the parent app
                "createdAt" to Timestamp.now(),
                "expiresAt" to Timestamp(System.currentTimeMillis() / 1000 + 3600, 0) // Expires in 1 hour
            )
            firestore.collection("pairingCodes").document(pairingCode).set(codeData).get() // Use .get() to wait for completion
        }

        // Ensure we are on PairingScreen
        composeTestRule.onNodeWithText(context.getString(R.string.pairing_screen_title)).assertIsDisplayed()

        // 2. Simulate UI interactions on PairingScreen
        // For a real PairingScreen, we'd have nodes for TextField and Button.
        // Using placeholders based on R.string IDs and assuming they exist in the actual UI.
        // This test will FAIL if PairingScreen.kt is not fully implemented with these elements.
        // Since PairingScreen.kt is a placeholder, this part is more of a template.

        // Let's assume PairingScreen.kt has a TextField with a test tag "PairingCodeTextField"
        // and a Button with test tag "PairingButton"
        // For now, we can't interact with these as they are not in PairingScreen.kt
        // composeTestRule.onNodeWithTag("PairingCodeTextField").performTextInput(pairingCode)
        // composeTestRule.onNodeWithTag("PairingButton").performClick()

        // **Conceptual Step**: If UI interaction were possible and successful:
        // The ViewModel would:
        // - Validate the code against Firestore (we've set it up to be valid)
        // - Call childIdRepository.saveChildId(childIdToSave)
        // - Delete the code from Firestore
        // MainActivity would then observe the new childId and navigate to LockScreen.

        // **Verification (Post-Conceptual Step):**
        // To make this test pass without full UI elements, we can manually trigger
        // what the UI interaction would do, IF we had access to the ViewModel or a way to simulate
        // the successful pairing event that leads to saving childId.
        // This is where the lack of DI or a more advanced test setup hurts.

        // For now, to demonstrate the navigation part *after* a successful pairing:
        // Let's manually save the childId to DataStore (simulating a successful pairing logic)
        // and then check if LockScreen appears.
        // This bypasses the actual UI interaction for pairing itself.
        runBlocking {
            // Get the DataStore instance from the DI container
            val childIdRepository = (composeTestRule.activity as MainActivity).let { activity ->
                // Since we can't easily access the injected repository from tests,
                // we'll use the AppModule approach directly
                val dataStore = com.google.pairing.di.AppModule.provideDataStore(context)
                ChildIdRepository(dataStore)
            }
            childIdRepository.saveChildId(childIdToSave)
        }
        // Relaunch activity to pick up the change from DataStore
        composeTestRule.activityRule.scenario.relaunch()


        // 3. Verify navigation to LockScreen
        val expectedLockScreenText = context.getString(R.string.lock_screen_message, childIdToSave)
        composeTestRule.onNodeWithText(expectedLockScreenText).assertIsDisplayed()

        // Cleanup: Delete the test pairing code from Firestore
        runBlocking {
            firestore.collection("pairingCodes").document(pairingCode).delete().get()
        }
    }
}
